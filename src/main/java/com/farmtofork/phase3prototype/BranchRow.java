package com.farmtofork.phase3prototype;

public class BranchRow {
    public int branchID;
    public String name;
    public String city;
    public String address;
    public String phone;

    public BranchRow(int branchID, String name, String city, String address, String phone) {
        this.branchID = branchID;
        this.name = name;
        this.city = city;
        this.address = address;
        this.phone = phone;
    }
}
